    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //validacion de campos
        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        } else if (txtApellido.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        } else if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        } else if (txtTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        } else if (txtDireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        } else if (txtUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        } else if (txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        } else {
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            String email = txtEmail.getText();
            String username = txtUsername.getText();
            String password = txtPassword.getText();
            String categoria = (String) comboNivel.getSelectedItem();
            String telefono = txtTelefono.getText();
            String direccion = txtDireccion.getText();
            datos = new String[][]{
                {nombre, apellido, email, username, password, categoria, telefono, direccion}};
                    
            try {
                new GestionEmpleados().registrarEmpleado();
            } catch (IOException ex) {
                Logger.getLogger(RegistrarDeEmpleado.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Empleado registrado con exito!");
            
            //limpiar campos
            txtNombre.setText("");
            txtApellido.setText("");
            txtEmail.setText("");
            txtUsername.setText("");
            txtPassword.setText("");
            comboNivel.setSelectedIndex(0);
            txtTelefono.setText("");
            txtDireccion.setText("");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

